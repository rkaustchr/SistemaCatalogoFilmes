    public frmVerUsuario() {
        initComponents();
        
        String nome;
        
        nome = (String)JOptionPane.showInputDialog(
                    new JFrame(),
                    "Informe o nome do usuário que deseja ver",
                    "Exibição de usuário",
                    JOptionPane.QUESTION_MESSAGE
                );
        
        if ( nome == "" ) {
            this.dispose();
            return;
        }
        
        // Procurar o usuário
        int total = SistemaCatalogoFilmes.usuarios.size();
        indice = -1;
        int i;
        
        for( i=0; i < total; i++ ) {
            if ( SistemaCatalogoFilmes.usuarios.get(i).getNome().equals( nome ) ) {
                indice = i;
                break;
            }
        }
        
        if ( indice == -1 ) {
            JOptionPane.showMessageDialog(this, "Usuário não encontrado!");
            this.dispose();
            return;
        }
        
        Usuario usuario = SistemaCatalogoFilmes.usuarios.get(indice);
        lblNome.setText(usuario.getNome());
        lblIdade.setText(String.valueOf( usuario.getIdade() ));
        lblGenero.setText(usuario.getGenero());
        lblOcupacao.setText(usuario.getOcupacao());
        lblCep.setText(usuario.getCep());
    }

